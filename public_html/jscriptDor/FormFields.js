function FormFields()
{
 if (document.myForm.Weight.value =="")
  {
    alert("Please enter Vehicle Weight");
    document.myForm.Weight.focus();
  } 
  else if (document.myForm.fuelType.value =="")
  {
    alert("Please enter Fuel Type");
    document.myForm.fuelType.focus();
  } 
  else if (document.myForm.unitNumber.value =="")
  {
    alert("Please enter Unit Number");
    document.myForm.unitNumber.focus();
  } 
 else if (document.myForm.insPolicy.value =="")
  {
    alert("Please enter Ins Policy");
    document.myForm.insPolicy.focus();
  } 
 else if (document.myForm.insCompany.value =="")
  {
    alert("Please enter Ins Company");
    document.myForm.insCompany.focus();
  }
 else if (document.myForm.axles.value =="")
  {
    alert("Please enter Axles");
    document.myForm.axles.focus();
  }
 else if ((document.myForm.axles.value < 2)  || (document.myForm.axles.value > 8))
  {
    alert("AXLES should be between 2 and 8");
    document.myForm.axles.focus();
  }
 else
	    {  
		   myForm.submitted = true;	
	       myForm.submit();
        }
 }