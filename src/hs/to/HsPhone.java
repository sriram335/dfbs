package hs.to;

import java.io.Serializable;


public class HsPhone extends HsEntity implements Serializable {

    private String number;

    public HsPhone() {
    }

    public HsPhone(String xnumber, String xtype) {
        this.setNumber(xnumber);
        this.setType(xtype);
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String newNumber) {
        number = newNumber;
    }


}
