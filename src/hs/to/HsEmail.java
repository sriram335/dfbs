package hs.to;

import java.io.Serializable;


public class HsEmail extends HsEntity implements Serializable {
    private String email;

    public HsEmail() {
    }

    public HsEmail(String xemail, String xtype) {
        this.setEmail(xemail);
        this.setType(xtype);
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String newEmail) {
        email = newEmail;
    }
}
