package util.logging;
/**
 * Title:Interface IDHSLogger
 * Description: This interface defines a generic logger for the project.
 * @author Dev Nimmagadda
 *
 */
public interface IDHSLogger {
	  /* This convenience method creates a logging message with the specified
	     * parameters and logs it to the undrlining logging system.
	     * @param category the category or the logging area the message belongs to.
	     * @param level the severity level of the logging message.
	     * @param srcClass the name of the class in which the logging is invoked.
	     * @param srcMethod the name of the method in which the logging is invoked.
	     * @param msg the custom message.
	     * @param t the Throwable associated with the message.
	     */
	    public void log(String mModuleName, int mLevel, String mClassName, String srcMethod, String mCustomMessage, Throwable mThrowable);
	    /**
	     * This convenience method creates a logging message with the specified
	     * parameters and logs it to the undrlining logging system.
	     * @param mModuleName the mModuleName or the logging area the message belongs to.
	     * @param mLevel the severity mLevel of the logging message.
	     * @param mClassName the name of the class in which the logging is invoked.
	     * @param srcMethod the name of the method in which the logging is invoked.
	     * @param MCustomMessage the custom message.
	     */
	    public void log(String mModuleName, int mLevel, String mClassName, String mMethodName, String mCustomMessage);
	    
	   
}
